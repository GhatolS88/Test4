package Casting;

public class Implicit_casting
{
	public static void main(String[] args)
	{
		// Primitive Casting
		//Converting lower data byte info into higher data byte info	
       int  a=10;          // 4 byte  // Numeric+ non-decimal;
       System.out.println(a);
       double b=a;               // 8 byte  //Numeric+decimal;
       System.out.println(b);
}
}  
