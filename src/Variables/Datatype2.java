package Variables;

public class Datatype2

{
	public static void main(String[]args)
	{ 
		//Step-I:Variable Declaration
		
	byte a;    
	short b;
	int c ;
	long d;
	float e;
	double f;
	char g;
	boolean h;
	String i;
	
	//Step-II:Variable Initialization
	a=100;// -128 to 127;
	b=30000;//-32768 to 32767;
	c=24367776;//-2,147,483,648 to 2,147,483,648;
	d=123456791234565L;
	e=100.56f;
	f=288967644.7;
	g='B';
	h=false;
	i="my name is sonali";
	
	
	//Step-III:Usage
	System.out.println(a); //100;
	System.out.println(b);//30000;
	System.out.println(c);//24367776
	System.out.println(e);
	System.out.println(f);
	System.out.println(g);
	System.out.println(h);
	System.out.println(i);
	}
	

}
