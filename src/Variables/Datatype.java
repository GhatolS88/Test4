package Variables;

public class Datatype

{
	
public static void main(String[]args)
{
//Step-I:Variable Declaration
	byte a;
	short b;
	int c;
	//Step-II:Variable Initialization
	a=100;  //from -128to127
	b=30000;//from -32768 to 32767
	c=24364626; // from -2,147,483,648 to 2,147,483,648
	//Step-III: Usage
	System.out.println(a); //100
	System.out.println(b); //30000
	System.out.println(c);//24364626
}
	
	
}
