package Control_Satement;

public class else_if
{
public static void main(String[]args)
{
	int Mark=75 ;
	if (Mark>=80)
	{
		System.out.println("Shital pass in exam with distinction");
	}
	else if(Mark>=70)
	{
		System.out.println("Shital pass in exam in first class");
	}
	else if(Mark>=60)
	{
		System.out.println("Shital pass in exam in Second class");
	}	
	else if(Mark>=50)
	{
		System.out.println("Shital pass in exam ");
	}
	else
	{
		System.out.println("Shital Fail in exam ");
	}
}
}
