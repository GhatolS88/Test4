package for_loop;

public class for_loop1
{
	// print the no form 76 to 87; ------------->76,77,78,79,80,81,82,83,84,85,86,87;
	public static void main(String[]args)
	{
	// i=76     76<=87 i=76+1
		       //  78<=87  i=78+1;
		        //79<=87   i=79+1
		        //80<=87  i=80+1;
		        //81<=87   i=81+1;
		//82<=87          i=82+1;
		//83<=87       i=83+1;
		//84<=87       i=84+1;
		//85<=87       i=85+1;
		//86<=87       i=86+1;
		//87<=87       i=87+1;
		//88<=87        i=87+1;
	for (int i=76;  i<=87;  i=i+1)
	{
		System.out.println(i);
	}

}
}
