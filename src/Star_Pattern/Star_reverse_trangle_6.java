package Star_Pattern;

public class Star_reverse_trangle_6 
{	
	public static void main(String[] args) 
	{	
		int star=5;
				
		for(int i=1; i<=5; i++)
		{

				
			for(int a=1; a<=star; a++)	
		{
			System.out.print("*");
		}
				
		System.out.println();		
			star--;
		}
		
	}

}
